#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    // Keywords
    Fn,
    Let,
    Mut,
    If,
    Else,
    Switch,
    Case,
    For,
    While,
    Do,
    Foreach,
    Class,
    Struct,
    Enum,
    Public,
    Private,
    Return,
    Break,
    Continue,
    Fallthrough,
    Import,
    Use,
    As,
    Package,
    Request,
    Require,
    Type,

    // Identifiers and literals
    Identifier(String),
    Int(i64),
    Double(f64),
    Char(char),
    String(String),
    Bool(bool),

    // Operators
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Equal,
    NotEqual,
    GreaterThan,
    GreaterThanOrEqual,
    LessThan,
    LessThanOrEqual,
    Not,
    And,
    Or,
    Xor,
    LeftShift,
    RightShift,
    Assign,
    PlusAssign,
    MinusAssign,
    StarAssign,
    SlashAssign,
    PercentAssign,
    LogicalAnd,
    LogicalOr,
    Increment,
    Decrement,
    Ampersand,
    Question,

    // Punctuation
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Comma,
    Colon,
    Semicolon,
    Dot,
    Arrow,

    // Other
    Comment,
    Illegal,
    Eof,
}
